<p class="">講師活動をすることは、<strong>自分自身の学習</strong>になり、知識が定着します。また、収益を稼ぐことができるのはもちろん、教えた受講生の中から「制作もお願いしたい」という形で<strong>制作の仕事に繋がっていく</strong>こともあります。</p>
