<!-- wp:paragraph -->
<p class="">数WordPressでのホームページ制作スキルを活かして、日々奮闘されているあなたへ。突然ですが、こんな風に感じたことはありませんか？</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">「今月はもうこれ以上、新しい案件は受けられないな…」</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">私自身、長年WordPressを使ったホームページ制作に携わってきましたが、常にこの「限界」と戦ってきました。どれだけスキルを磨いても、一人で対応できる案件数には限りがあり、すぐにリソースがパンクしてしまう。もっと収入を増やしたいのに、物理的に不可能だ、と。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">しかし、もしあなたのそのスキルを「制作」ではなく「教育」という形で提供するとしたら、世界は一変します。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">ホームページ制作と教育ビジネス。両者の最大の違いは、<strong>「時間」と「規模」の概念</strong>にあります。</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li><strong>制作業務:</strong> 終わりは「納品が完了するまで」。クライアントの要望に応え続ける限り、あなたの時間はどこまでも費やされます。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>教育ビジネス:</strong> 終わりは「決めた時間」が来た時。2時間のセミナーなら、2時間で完了です。</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p class="">さらに、規模の面でも圧倒的な差があります。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">制作は基本的に1対1の仕事ですが、教育であれば、セミナー形式で「1対100」の価値提供<strong>も可能です。もし動画教材にしてしまえば、その可能性は</strong>「無限大」に広がります。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">もちろん、制作と同じく原価がかからない（仕入れが不要）という強みも健在です。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">この講座では、まさにこの「教育ビジネス」の可能性を、WordPressのプロであるあなたがどう実現していくのか、その具体的な方法を解説していきます。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">かつての私と同じように「制作業務の限界」を感じているあなたへ。その卓越したスキルを、もっと自由で、もっと大きな可能性を秘めた形で活かす方法を、ここから一緒に学んでいきましょう。</p>
<!-- /wp:paragraph -->

