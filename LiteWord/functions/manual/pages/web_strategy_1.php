<h2 class="wp-block-heading is-style-h_01">第5章：【運用編】サイトは「公開」してからがスタート</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p class="">おめでとうございます。これであなたのサイトは公開できる状態になりました。 しかし、本当のスタートはここからです。家が完成しても、そこに人を招かなければ意味がありません。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3,"className":"is-style-h_02"} -->
<h3 class="wp-block-heading is-style-h_02">STEP 10: 集客（導線）設計の実行</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p class="">サイトは「作っただけ」では、大海原に浮かぶ孤島と同じで、誰も訪れてくれません。サイトの「手前」で、どうやってペルソナにあなたのサイトの存在を知らせ、訪問してもらうか。この「集客導線」の設計と実行が不可欠です。</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li><strong>SNSでの訴求（告知）</strong><!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li>あなたが普段使っているSNS（X, Instagram, Facebookなど）で、サイトを作ったことを告知します。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>ただ「サイトを作りました」と報告するだけでは不十分です。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>「（ペルソナ）の皆さんへ。あなたが抱える（悩み）を解決するための（サービス内容）について、詳しくまとめたサイトを公開しました。今なら〇〇も実施中です。プロフィール（@account）のリンクからご覧ください」</strong></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>このように、ペルソナのベネフィットと絡めて告知します。</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>既存のメディアからの誘導</strong><!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li>ブログ、note、YouTubeなどを運用しているなら、全ての記事や動画の末尾、概要欄に「詳しくは公式サイトへ」とリンクを貼ります。</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>オフラインからの誘導</strong><!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li>名刺、チラシ、パンフレットなどにも必ずサイトURLとQRコードを記載します。</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></li>
<!-- /wp:list-item --></ul>
