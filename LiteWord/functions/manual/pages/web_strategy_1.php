<!-- wp:paragraph -->
<p class=""><strong>Webサイトは「作る」前に9割決まる</strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">今、あなたはこのガイドを「自分のWebサイトを作りたい」「もっと伝わるサイトにリニューアルしたい」という想いで開いていることでしょう。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">多くの人がWebサイト制作と聞くと、まず「どんなデザインにしようか」「どのツールを使おうか」といった「作り方」に目が行きがちです。しかし、どれだけ美しいデザインのサイトを作っても、訪問者に「あなたが何者で、私に何をしてくれるのか」が一瞬で伝わらなければ、そのサイトは存在しないのと同じです。訪問者は、理解できないサイトからは3秒で離脱してしまいます。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">Webサイト制作の成功は、コーディングやデザインの技術力以前に、<strong>「公開前の戦略・設計段階」で9割が決まります。</strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">このガイドの目的は、単なるツールの使い方を説明することではありません。訪問者の心に響き、あなたの価値が正しく「伝わる」サイトを、戦略的に、そして効率的に作り上げるための「思考の流れ」と「具体的なステップ」を、順を追って徹底的に解説することです。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">この流れに沿って一つずつ進めていけば、あなたは「ただ存在するだけのサイト」ではなく、「目的を達成し、訪問者を導き、あなたのビジネスを力強く後押ししてくれるサイト」を手に入れることができるはずです。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"className":"is-style-h_01"} -->
<h2 class="wp-block-heading is-style-h_01">第1章：【戦略編】サイト制作の「核」を決める</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p class="">サイト制作は、家づくりに似ています。いきなり壁紙を選んだり、インテリアを買い揃えたりする人はいません。まずは「誰が住むのか？」「何のための家か？」「何部屋必要か？」という「設計の核」を決めます。Webサイトもまったく同じです。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3,"className":"is-style-h_02"} -->
<h3 class="wp-block-heading is-style-h_02">STEP 1: 目的の明確化 (Why) - なぜ、このサイトを作るのか？</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p class="">全ての行動は、この「なぜ」から始まります。ここが曖昧なまま進めると、途中で「何をアピールしたいんだっけ？」と軸がブレてしまい、誰にも何も伝わらないサイトが出来上がります。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">まずは、あなた自身に深く問いかけてください。</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li><strong>あなたは何のために、このサイトを作るのですか？</strong></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p class="">この答えは、サイトの「ゴール（目的）」となります。</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li><strong>例：コンサルティングサービスを提供したい場合</strong><!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li>（悪い例）：なんとなく、自分のサービスを紹介するため。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>（良い例）：「集客に悩む小規模な美容室オーナー」<strong>が、私の</strong>「リピート率向上に特化したコンサルティング」<strong>に興味を持ち、</strong>「初回無料相談」に申し込んでくれること。</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p class="">このように、「誰に」「何を」提供し、「最終的にどんな行動（コンバージョン）をしてほしいか」を、一つの文章で言えるレベルまで具体化します。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">この「最終的な行動（コンバージョン）」の例としては、以下のようなものが考えられます。</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li>お問い合わせフォームからの送信</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>無料相談・体験セッションへの申し込み</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>商品・サービスの購入</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>資料請求・PDFダウンロード</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>メールマガジンへの登録</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>電話をかけてもらう</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p class="">この「ゴール」が明確であればあるほど、後のSTEP 2（ペルソナ設定）やSTEP 5（要件定義）で、「ゴール達成のために、何を伝えるべきか」が自動的に決まっていきます。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3,"className":"is-style-h_02"} -->
<h3 class="wp-block-heading is-style-h_02">STEP 2: ターゲット（ペルソナ）の明確化 (Who) - 誰に、届けたいのか？</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p class="">STEP 1で定めたゴールを達成するために、次に「誰に」その情報を届けるのかを徹底的に掘り下げます。これが「ペルソナ設定」です。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">「ターゲット」が「30代の女性」といった広い層を指すのに対し、「ペルソナ」は「特定のたった一人」の架空の人物像を指します。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">なぜ「たった一人」に絞る必要があるのでしょうか？ それは、「すべての人に向けたメッセージは、誰の心にも響かない」からです。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">例えば、「皆さん、健康に気を使いましょう」という呼びかけよりも、「最近、仕事の残業続きで、深夜にコンビニ弁当ばかり食べている山田さん（32歳・男性・独身）。お腹周りが気になり始めたけど、運動する時間がないと悩んでいませんか？」と呼びかけられた方が、ドキッとする人は明確です。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">あなたのサイトは、この「たった一人のペルソナ」に向けて書くラブレターのようなものです。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":4,"className":"is-style-h_03"} -->
<h4 class="wp-block-heading is-style-h_03"><strong>ペルソナ設定の具体的な項目例：</strong></h4>
<!-- /wp:heading -->

<!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li><strong>基本情報</strong>：名前、年齢、性別、職業、役職、居住地、家族構成</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>価値観・性格</strong>：何を大切にしているか？（価格、品質、権威性、手軽さ？）</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>情報収集の方法</strong>：普段、何を見ているか？（Instagram、X(Twitter)、YouTube、Google検索、専門誌？）</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>サイト訪問時の状況</strong>：どんなキーワードで検索して、どんな気持ちでこのサイトにたどり着いたか？</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>最も重要な項目：悩み、課題、欲求 (Pain Point)</strong><!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li>今、何に一番困っているか？</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>何を恐れているか？</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>何を達成したいと（心の底で）望んでいるか？</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p class="">このペルソナが、あなたのサイトの「羅針盤」となります。これから先のすべてのステップ（どんなページを作るか、どんな文章を書くか、どんなデザインにするか）で迷った時は、必ずこのペルソナに立ち返り、「彼（彼女）は、この情報で喜ぶだろうか？」「この言葉で伝わるだろうか？」と自問自答してください。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"className":"is-style-h_01"} -->
<h2 class="wp-block-heading is-style-h_01">第2章：【設計編】サイトの「骨組み」を作る</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p class="">家の設計図を描く段階です。ペルソナという「住人」が決まったので、次は「住人」が快適に過ごせるように、部屋割り（サイトマップ）と、各部屋の家具配置（ワイヤーフレーム）を決めていきます。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3,"className":"is-style-h_02"} -->
<h3 class="wp-block-heading is-style-h_02">STEP 3: 必要なページの洗い出し (サイトマップ作成)</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p class="">ペルソナの悩みを解決し、STEP 1で決めたゴール（例：お問い合わせ）に導くために、どんな「部屋」（ページ）が必要かを考えます。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">まずは難しく考えず、必要なページを箇条書きでざっくりと書き出してみましょう。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":4,"className":"is-style-h_03"} -->
<h4 class="wp-block-heading is-style-h_03"><strong>【必須ページ（家でいう玄関・リビング・寝室）】</strong></h4>
<!-- /wp:heading -->

<!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li><strong>トップページ</strong>：サイトの「顔」であり「目次」。訪問者が最初に着地し、何者かがわかる場所。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>プロフィール（私について）</strong>：あなたが「何者」で「なぜ信頼できるか」を伝える場所。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>サービス内容（商品・料金）</strong>：「何を提供してくれるか」を具体的に説明する場所。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>お問い合わせフォーム</strong>：ゴールとなる行動を起こしてもらう場所。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>プライバシーポリシー</strong>：個人情報の取り扱いを明記する、信用のために必須のページ。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>（物販や有料サービスの場合）<strong>特定商取引法に基づく表記</strong></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:heading {"level":4,"className":"is-style-h_03"} -->
<h4 class="wp-block-heading is-style-h_03"><strong>【あると信頼性が高まるページ（書斎・応接室）】</strong></h4>
<!-- /wp:heading -->

<!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li><strong>お客様の声・実績紹介</strong>：第三者の評価で「信頼」を補強する場所。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>よくあるご質問 (FAQ)</strong>：訪問者の「疑問」を先回りして解消し、不安を取り除く場所。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>ブログ・お知らせ</strong>：専門知識（価値提供）や人柄を伝え、関係性を築く場所。</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p class="">これらのページをリストアップしたら、それらを「サイトマップ」と呼ばれる構造図に整理します。トップページを頂点に、どのページからどのページへ移動できるようにするか、サイト全体の地図を描きます。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3,"className":"is-style-h_02"} -->
<h3 class="wp-block-heading is-style-h_02">STEP 4: ページごとの「要件定義」 (各部屋に必要な要素)</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p class="">サイトマップで「部屋」が決まったら、次は「その部屋に何を置くか」を決めます。これが「要件定義」です。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">STEP 2で設定したペルソナが、そのページを訪れた時に「何を知りたいか？」「どんな情報があれば納得して次の行動に移れるか？」を考え、各ページに必要な「情報（コンテンツ）の要素」を箇条書きでリストアップします。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":4,"className":"is-style-h_03"} -->
<h4 class="wp-block-heading is-style-h_03"><strong>例：トップページの要件定義（箇条書き）</strong></h4>
<!-- /wp:heading -->

<!-- wp:list {"ordered":true,"start":1} -->
<ol start="1" style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li><strong>ファーストビュー（最上部）</strong><!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li>キャッチコピー（誰の・どんな悩みを解決できるか）</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>権威性・実績（例：〇〇専門、実績100件以上）</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>行動喚起(CTA)ボタン（例：「無料相談はこちら」「サービス一覧」）</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>悩みへの共感</strong><!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li>「こんなことで悩んでいませんか？」（ペルソナの悩みを具体的に列挙）</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>解決策の提示（サービス概要）</strong><!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li>その悩みを、私のサービスがどう解決できるか（ベネフィット）</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>各サービスへの導線（「詳しくはこちら」ボタン）</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>選ばれる理由（強み）</strong><!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li>競合他社ではなく、なぜあなたを選ぶべきか（独自の強み、USP）</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>信頼の証拠</strong><!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li>お客様の声（抜粋）、実績紹介（ロゴなど）</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>プロフィール（自己紹介）</strong><!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li>運営者の顔写真、簡潔な自己紹介、理念</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>「プロフィール詳細へ」ボタン</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>行動喚起（クロージング）</strong><!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li>最後にもう一度、ゴールへの導線（お問い合わせ、無料相談など）</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></li>
<!-- /wp:list-item --></ol>
<!-- /wp:list -->

<!-- wp:heading {"level":4,"className":"is-style-h_03"} -->
<h4 class="wp-block-heading is-style-h_03"><strong>例：プロフィールページの要件定義（箇条書き）</strong></h4>
<!-- /wp:heading -->

<!-- wp:list {"ordered":true,"start":1} -->
<ol start="1" style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li>キャッチコピーと名前、肩書き</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>人柄が伝わるメイン写真</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>理念・ミッション（私が大切にしていること）</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>ストーリー（なぜこの仕事をしているのか？ 過去の経歴、苦労、転機）</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>実績・資格・メディア掲載履歴</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>プライベートな側面（趣味、家族など ※親近感を出す場合）</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>読者へのメッセージ、お問い合わせへの誘導</li>
<!-- /wp:list-item --></ol>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p class="">この要件定義をページごとに行うことで、<strong>「なんとなく」でコンテンツを作ることを防ぎ</strong>、ペルソナをゴールへ導くために必要な情報が漏れなく配置された、論理的なページ構成が完成します。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3,"className":"is-style-h_02"} -->
<h3 class="wp-block-heading is-style-h_02">STEP 5: ワイヤーフレームの作成 (サイトの設計図)</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p class="">要件定義で「部屋に置く家具（情報要素）」が決まったら、いよいよ「家具の配置図（レイアウト）」を描きます。これが「ワイヤーフレーム」です。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">ワイヤーフレームとは、デザイン（色や装飾）を一切排除し、Webページの「どこに」「何を」配置するかを、線や四角、テキストだけで示す「設計図」です。紙と鉛筆で描いても、専用ツールを使っても構いません。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":4,"className":"is-style-h_03"} -->
<h4 class="wp-block-heading is-style-h_03"><strong>なぜワイヤーフレームが絶対に必要か？</strong></h4>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p class="">いきなりデザイン（またはWordPressなどの構築）を始めると、必ず手戻りが発生します。文章を当てはめてみたら「長すぎてレイアウトが崩れた」「ここの順番を入れ替えたいが、もう作ってしまった」といった問題が起こるからです。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">ワイヤーフレームを作成する最大のメリットは、<strong>「文章の文字量」と「レイアウト」のバランスを、実装前に視覚的に調整できる</strong>ことです。</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li><strong>文字量の調整</strong>： 例えば「3つの特徴」を横並びで紹介するレイアウト（ワイヤー）を描いたとします。1つ目の特徴の説明が50文字なのに、2つ目が300文字もあったら、どう考えてもバランスが崩れて見にくくなります。ワイヤーフレームの段階でそれに気づけば、「この枠に収まるよう、各特徴を100文字程度でまとめる」というルールが作れます。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>情報の優先順位の確認</strong>： 要件定義（箇条書き）だけではわからなかった「情報の流れ」を視覚化できます。「やはり、悩みへの共感より先に、サービス概要を見せた方が早いかもしれない」といった調整が、この段階で簡単に行えます。</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p class="">「文章をざっくり作る → ワイヤーフレームに当てはめてみる → ワイヤーに合わせて文章の長さを調整する」</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">この流れこそが、手戻りを最小限にし、最も効率的に「伝わる」レイアウトと文章を両立させる秘訣です。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"className":"is-style-h_01"} -->
<h2 class="wp-block-heading is-style-h_01">第3章：【制作編】サイトに「魂」を吹き込む</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p class="">設計図が完成したら、いよいよ家の建築と内装（コンテンツ制作）に入ります。ここでの主役は「文章（ライティング）」です。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3,"className":"is-style-h_02"} -->
<h3 class="wp-block-heading is-style-h_02">STEP 6: コンテンツ（文章・画像）の作成</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p class="">Webサイトの価値は、デザインではなく「コンテンツ」、特に「文章」で決まります。なぜなら、訪問者はあなたの文章を読んで「信頼できるか」「自分に合っているか」を判断するからです。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":4,"className":"is-style-h_03"} -->
<h4 class="wp-block-heading is-style-h_03"><strong>ライティングの鉄則：「伝わる」文章術</strong></h4>
<!-- /wp:heading -->

<!-- wp:list {"ordered":true,"start":1} -->
<ol start="1" style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li><strong>とにかく短く、簡潔に</strong> 現代のユーザーは、基本的に長い文章を読みません。流し読みされます。 一つの文章は短く（句読点「、」を減らす）、「多くても2行以上は書かない」くらいの意識で、テンポよく改行を入れましょう。短い文章を積み重ねていくことで、結果的に読み進めてもらえます。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>ペルソナに「語りかける」</strong> STEP 2で設定した「たった一人」のペルソナに向けて、面と向かって語りかけるように書きます。「皆様」ではなく「あなた」を主語にします。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>専門用語を避け、中学生でもわかる言葉で</strong> あなたが当たり前に使っている業界用語は、ペルソナには伝わりません。できる限り平易な言葉に置き換えましょう。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>メリット（機能）ではなく、ベネフィット（未来）を伝える</strong><!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li>メリット（機能）：例「このPCはメモリが32GBです」</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>ベネフィット（未来）：例「動画編集とオンライン会議を同時に行っても、カクつかずに快適に作業できます」 訪問者が欲しいのは機能ではなく、「そのサービスによって自分の悩みが解決され、どんな素晴らしい未来が手に入るか」です。</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></li>
<!-- /wp:list-item --></ol>
<!-- /wp:list -->

<!-- wp:heading {"level":4,"className":"is-style-h_03"} -->
<h4 class="wp-block-heading is-style-h_03"><strong>AI活用のコツと「自分らしさ」の両立</strong></h4>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p class="">構成案や文章の「たたき台」作成にAIを活用することは、非常に効率的です。AIにペルソナ情報と要件定義を渡せば、大枠の文章を生成してくれますし、自分の考えを整理する壁打ち相手にもなります。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">ただし、<strong>絶対にAIが生成した文章をそのまま使ってはいけません。</strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">AIの文章は無機質で、誰にでも当てはまる平均的な内容になりがちです。そのままでは、あなたの「人柄」や「熱意」、「独自の経験」が伝わらず、他のサイトとの違いが生まれません。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">AIはあくまで「効率化のためのアシスタント」です。AIが作った大枠をベースに、必ず<strong>あなた自身の言葉で「改善」し、あなた自身の経験談や想いを込める（魂を吹き込む）作業</strong>を行ってください。それこそが「あなたらしさ」であり、訪問者が信頼を寄せるポイントになります。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class=""><strong>画像・写真の準備</strong> 文章と同様に、写真も重要です。フリー素材は便利ですが、多用すると「どこかで見たサイト」という印象を与えます。特にプロフィール写真やサービス提供中の写真は、あなたの人柄や専門性を伝えるために、できるだけオリジナルの（できればプロに依頼した）写真を使うことを強く推奨します。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3,"className":"is-style-h_02"} -->
<h3 class="wp-block-heading is-style-h_02">STEP 7: デザインと実装（サイトの構築）</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p class="">ワイヤーフレームとコンテンツ（文章・画像）が揃ったら、これらを組み合わせて実際のWebサイトとして構築（実装）していきます。</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li><strong>デザインの原則</strong>：<!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li><strong>ペルソナに合わせる</strong>：ペルソナが好む世界観（例：信頼感のある青基調、温かみのあるオレンジ基調）に合わせます。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>読みやすさ優先</strong>：デザイン性よりも、文字の大きさ、行間、余白の取り方など、「文章の読みやすさ」を最優先します。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>スマホ対応（レスポンシブ）</strong>：必須です。PCよりスマートフォンで閲覧されることの方が多いと考え、スマホでの見え方を基準に設計します。</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></li>
<!-- /wp:list-item --></ul>
