<!-- wp:heading {"className":"is-style-h_01"} -->
<h2 class="wp-block-heading is-style-h_01">第4章：【レビュー編】サイトを「客観的」に見直す</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p class="">家が完成したら、住み始める前に「内覧会」をします。サイトも同じで、公開前に徹底的なチェックが必要です。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3,"className":"is-style-h_02"} -->
<h3 class="wp-block-heading is-style-h_02">STEP 8: ペルソナ視点での最終レビュー</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p class="">ここが、伝わるサイトと伝わらないサイトの最後の分岐点です。 <strong>あなた自身が、STEP 2で設定した「ペルソナ」に完全になりきって</strong>、サイトを初めて訪れた客として操作してみます。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p class="">自分のサイトを「制作者」の目線で見るのではなく、悩みを抱えた「訪問者」の目線で、厳しく俯瞰してチェックしてください。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":4,"className":"is-style-h_03"} -->
<h4 class="wp-block-heading is-style-h_03"><strong>チェックリスト</strong></h4>
<!-- /wp:heading -->

<!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li>トップページを開いて3秒で、「何者で、何を提供しているか」が直感的にわかりますか？</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>使われている言葉遣いは、ペルソナに響きますか？ 上から目線だったり、難しすぎたりしませんか？</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>情報の順番（構成）は、ペルソナが知りたい順番になっていますか？</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>各ページを読み終えた後、「次に何をすればいいか」が明確ですか？（ボタンはわかりやすいか？）</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>「トップへ戻る」ボタンやフッターメニューは適切に配置され、迷子になりませんか？</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>すべてのリンクは正しく機能しますか？（リンク切れはありませんか？）</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>お問い合わせフォームは正常に動作し、送信控えメールは届きますか？</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>スマホで見た時に、文字が小さすぎたり、ボタンが押しにくかったりしませんか？</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p class="">このレビューで見つかった違和感は、訪問者も必ず感じる違和感です。一つずつ丁寧に潰していきましょう。 このレビュー（俯瞰）は、<strong>STEP 5のワイヤーフレームが完成した段階</strong>でも一度行っておくと、「文章の段階」で構成のズレに気づけるため、手戻りを最小限に抑えられます。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3,"className":"is-style-h_02"} -->
<h3 class="wp-block-heading is-style-h_02">STEP 9: 内部SEOの基本設定</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p class="">SEO（検索エンジン最適化）は非常に奥深い分野ですが、最低限の設定は公開前に行います。</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul style="list-style-type:undefined" class="wp-block-list"><!-- wp:list-item -->
<li><strong>クローラー（ロボット）の視点</strong>： Googleの検索ロボット（クローラー）が、あなたのサイトをスムーズに巡回できるようにします。STEP 3のサイトマップに基づき、全てのページがどこかからリンクで辿り着ける（孤立していない）状態にします。特に「トップページ ⇔ 仮想ページ」のリンクがしっかり繋がっていることが重要です。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>タイトルタグ、メタディスクリプション</strong>： 各ページに、Googleの検索結果に表示される「タイトル」と「説明文」を設定します。ペルソナが検索しそうなキーワードを含めつつ、クリックしたくなるような魅力的な内容にします。</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

