<h3 class="wp-block-heading is-style-h_03" id="776cf6cb-2abb-41bc-92f8-c979adee91df">4. 比較・検討</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p id="5b09b672-f624-48bc-8ca9-ae9a4534e34d" class="">お客様が、他社のサービスとあなたのサービスを比べる段階です。「あなたから買う理由」が明確に伝わるように準備しておきましょう。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="9e5d397e-4f53-432a-b1ca-a4513cba5c69" class="">お客様の声を見せたり、サービスの詳細な料金や特徴を分かりやすく伝えたりすることが重要です。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3,"className":"is-style-h_03"} -->
<h3 class="wp-block-heading is-style-h_03" id="10fd2d11-2ad0-4b84-825b-7e0fdfb27ecd">5. 行動（購入・申込）</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p id="1156ec04-f902-4819-858e-5e1dbc4656ff" class="">お客様が、最終的に「買います！」と決断する段階です。お客様が迷ったり、面倒に感じたりしないよう、スムーズに購入できる流れを用意しましょう。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="be026f07-15f8-4b14-950b-1bd90071daa4" class="">分かりやすい申込フォームの作成や、決済方法を複数用意することなどがこの部分です。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3,"className":"is-style-h_03"} -->
<h3 class="wp-block-heading is-style-h_03" id="21213f22-ff70-4f6b-9b52-e946f3ac6e21">6. 継続・紹介</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p id="4fd0f7ca-2f6b-4cec-a82f-f03de45c80cc" class="">一度購入してくれたお客様と、良い関係を続けていく段階です。どうすればお客様がリピーターになり、さらには口コミで他の人に紹介してくれるかを考えましょう。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="b527751f-0a9a-4bce-ac11-7093bf1d9ca7" class="">購入後のアフターフォローや、リピーター限定の特典を用意することなどがこれにあたります。</p>
