<p id="5e64e660-0eec-4eca-8c3a-3e6f8a1bf0e8" class="">時々、利用者の方とお会いする機会があるのですが、多くの方が心から喜んでくださっている様子を目の当たりにすると、「作って本当に良かった」と実感し、さらなる開発へのモチベーションが湧いてきます。 「これから色々な人に紹介しますね」と約束してくださる方もいて、そのお気持ちだけでも本当に嬉しく、日々の励みになっています。</p>
