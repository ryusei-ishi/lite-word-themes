<!-- /wp:image -->

<!-- wp:paragraph -->
<p id="1d88ff7a-3111-4719-936d-11fc95733ddf" class="">まず、ホームページを作るぞ！と思った方が、LiteWordのテーマを導入をタイミングは赤色の枠で囲まれている、「WordPressのテーマは何にしよう？？」と悩むタイミングです。</p>
