<!-- wp:list-item -->
<li><strong>リード獲得</strong><br>メールフォームからLiteWordテーマデータのダウンロード<br>（LiteWordのテーマを利用するためにダウンロードしてもらう）</li>
<!-- /wp:list-item --></ol>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p id="96e6396e-4914-4476-9e83-1b3c4c9381f3" class="">拘りポイントは、LiteWordの宣伝をしていことです。<br>LiteWordはあくまでWordPressでサイトを簡単に作るために、使うテーマであり、それを利用するにはテーマをサイトからダウンロードしないといけない。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="6ab7a25a-f65d-4315-888f-1e45f8e81f85" class="">サイトに訪れる前に、すでにダウンロードする事が確定しているのです。<br>そして、信頼はYouTubeを視聴してる段階で担保されているのです。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="9dfdfd3e-54dd-496b-8948-bd762fc00ff3" class="">これがLiteWordのスタート戦略です。</p>
