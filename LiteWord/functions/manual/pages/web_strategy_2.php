<!-- wp:heading {"level":3,"className":"is-style-h_03"} -->
<h3 class="wp-block-heading is-style-h_03" id="67bcd3ed-e081-4bf2-b2f6-4aa921fdc5bb">◆なぜこの順番で設計するのか？</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p id="3718ff8e-f479-4f8f-a76c-e3b393115257" class="">多くの人は、思いつきでSNSの投稿を始めてしまいます。しかし、それでは「発信に一貫性がない」「成果につながらない」という状態に陥りがちです。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="b148faa5-b685-4143-adf6-6c145cce36a3" class="">このシートは、<strong>サービスを起点に、それを必要とする人を明確にし、その人に響く導線を逆算して設計する</strong>ことで、筋の通った強力なマーケティングを実現します。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3,"className":"is-style-h_03"} -->
<h3 class="wp-block-heading is-style-h_03" id="9a5aa716-ec88-4473-9b0c-25b2a5b853e1">◆具体的な記入手順</h3>
