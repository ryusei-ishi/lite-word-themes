<h3 class="wp-block-heading is-style-h_03" id="d8f436d2-0dd4-4677-a9ce-ef526daa01e0">「今すぐ欲しい」人に届ける難しさ</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p id="cba63e87-772b-4817-aff2-1e72e9bb5154" class="">当時は「無料で高機能であれば、みんな使ってくれるだろう！どんどん拡散されるだろう」と安易に考えていました。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="c78722f9-cb5a-467d-ae33-91de73285c71" class="">しかし、まったくそんな事はありませんでした。<br>私自身も知り合いに伝えても、誰一人として使ってくれませんでした。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="d4a53509-005d-4130-9205-eaf1accb06e7" class="">理由は簡単です。<br>無料であっても<strong>いまは必要ない</strong>からです。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="a3860ec1-e240-45c5-8a30-1e00370bcc0f" class="">「今すぐサイトを作りたい」というタイミングで目の前に「LiteWord」が現れなければ、決してダウンロードされません。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="9cd926ad-9fa5-468b-9f44-1da61e4163c0" class="">さらに、<strong>「必要だ」と思ったその瞬間に「これが必要だ」と感じてもらわなければならない</strong>のです。これは非常にニッチで、難易度の高い集客なのです。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="1a998fc9-4dfa-4249-8e04-bca6f4f95a96" class="">下記の画像は、WordPressを導入するユーザーの簡易な流れです。</p>
