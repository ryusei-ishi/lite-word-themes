<p id="7ae3413e-88ca-4cb2-95b9-ef6d07e98b3c" class="">次にお話しするのは、アクセス数の管理についてです。 解析ツールとしてGoogle Analyticsは導入していますが、それ以上に重視していたのが、ダウンロードページの直前に設置した<strong>簡単なアンケート</strong>でした。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="29c262ac-78a4-498c-afbc-30938710472d" class="">「LiteWordをどこで知りましたか？」という質問に対し、「YouTube」「検索エンジン」「知人の紹介」といった選択肢をチェックボックスで用意し、大まかな流入経路を把握していました。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="587d0245-9ade-41c4-ac02-3a444ce97476" class="">その結果、現在最も多い流入経路は「知り合いからの紹介」<strong>で、次に多いのが</strong>「YouTube」です。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="c2173969-cce8-4de8-8af4-d2cd4dd1f33a" class="">これは、まずYouTubeでLiteWordが拡散され、それを見たインフルエンサーのような影響力のある方々が使ってくださったことがきっかけです。その方々がLiteWordを高く評価し、「無料だからぜひ使ってみて」と周囲に勧めてくれたことで、「知り合いからの紹介」という口コミでの流入が大きく増加しました。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="76d00346-4545-46fb-a396-3b62ed95780b" class="">Google Analyticsについては、「サイトにアクセスがあるか」「どのページがよく見られているか」といった全体の動向を把握する程度に留めています。毎日細かくデータを見るよりも、アンケートから得られる<strong>直接的な流入経路</strong>の方を重視していました。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="181ab83a-c094-4323-9c47-ee0614b3fb85" class="">もちろん、プロとしてはGoogle Analyticsを詳細に分析し、サイトを改善し続けることが理想です。しかし、リード獲得を目指す初期段階においては、データ分析に時間を費やすよりも、<strong>改善点が明確な箇所から着実に修正していくことの方が重要だ</strong>と考えています。</p>
