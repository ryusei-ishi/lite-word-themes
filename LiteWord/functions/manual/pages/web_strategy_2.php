<h3 class="wp-block-heading is-style-h_03" id="9e0563d7-ea40-4f7b-90a1-d38ca628e0e6">具体例：美容室に来店する高校生男子のカスタマージャーニー</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p id="d5e661c8-36bd-48a7-9734-a0a6d275db4e" class="">ここでは、添付の画像を基に「カッコよくなって女の子にモテたい高校生男子」をペルソナとしたカスタマージャーニーを見ていきましょう。（※現代の高校生に合わせ、SNSはTikTokを想定しています）</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="fbb7b123-11aa-4f73-a946-ac5825825597" class=""><strong>ステップ1：悩みと願望（きっかけ）</strong></p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul style="list-style-type:undefined" id="b67ba500-713c-4739-b514-19dd6020b829" class="wp-block-list"><!-- wp:list-item -->
<li><strong>悩み・願望</strong>：「カッコよくなって女の子にモテたい！彼女が欲しい！」</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>危機感</strong>：「このままでは高校生のうちに彼女ができないかもしれない…」</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p id="1b304350-9ee3-4f45-be4e-09130a641d49" class="">すべては、彼の純粋な悩みや願望から始まります。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="1b4e2c4b-e586-4187-b994-2a854b1d6aeb" class=""><strong>ステップ2：情報収集と解決策の発見</strong></p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul style="list-style-type:undefined" id="cf533b9f-e561-4e1c-aaac-ac1880d77f9d" class="wp-block-list"><!-- wp:list-item -->
<li><strong>思考</strong>：「どうすればカッコよくなれるんだろう？」</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>発見</strong>：「そうだ、まずは外見から。髪型をカッコよくすればいいんだ！」</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>行動</strong>：TikTokを開き、「#かっこいい髪型」などで検索。美容室が投稿している劇的なビフォーアフター動画をたくさん見て、「すごい！こんなに変わるんだ！」と感動し、興味を持ちます。</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p id="80039ff7-6bee-42c0-922b-c2bd0d7f27f5" class=""><strong>ステップ3：具体的な店舗探しと比較検討</strong></p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul style="list-style-type:undefined" id="df5b2aa4-835c-4f37-a0f7-85432c44f987" class="wp-block-list"><!-- wp:list-item -->
<li><strong>思考</strong>：「自分もあんな風になりたい！そうだ、イケてるカリスマ美容師を探そう！」</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>行動</strong>：再びTikTokで「#カリスマ美容師」「#（自分の地域名）美容室」といったキーワードで検索を開始。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>検討</strong>：気になる美容師のアカウントを見つけ、プロフィールや投稿されているヘアスタイルをチェック。さらに、プロフィールに貼られているウェブサイトのURLにアクセスし、実績やお店の雰囲気を確認して、期待感を高めます。</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p id="5f474c09-90e3-4324-b936-8a7085212fee" class=""><strong>ステップ4：見込み客化（LINE登録）</strong></p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul style="list-style-type:undefined" id="680f3d36-6465-44fe-a857-beac76f1923f" class="wp-block-list"><!-- wp:list-item -->
<li><strong>発見</strong>：ウェブサイトで「LINEで予約すると初回1000円割引！」という魅力的な特典を見つけます。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>行動</strong>：「今すぐ予約するわけではないけど、お得だからとりあえず登録だけしておこう」と考え、LINE公式アカウントを友だち追加します。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>状況</strong>：この時点では、まだ他の美容室とも比較検討している段階です。</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p id="10142bc5-d7e3-4574-89dd-bba116a7532d" class=""><strong>ステップ5：顧客育成（ナーチャリング）</strong></p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul style="list-style-type:undefined" id="81f35e63-d7d1-49ab-8606-7e11b4b7b9cd" class="wp-block-list"><!-- wp:list-item -->
<li><strong>接触</strong>：登録したLINEから、定期的にメッセージが届きます。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>情報</strong>：その内容は、「女子にモテる髪型トップ10（TikTok動画への誘導）」「いい匂いだと思われる、モテるシャンプーの選び方」「短髪でもカッコよく見せる秘訣」など、彼の「モテたい」という悩みに寄り添った情報ばかりです。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>感情の変化</strong>：「この美容室、すごくよく分かってるな！」「この美容師さんにお願いすれば、自分もモテるようになれるかもしれない！」と、お店への信頼と期待がどんどん高まっていきます。</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p id="ed11f41c-db0f-4cfe-8087-8f466868e1d8" class=""><strong>ステップ6：来店（コンバージョン）</strong></p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul style="list-style-type:undefined" id="8cb55680-2e38-414a-bf55-8bf10e21731d" class="wp-block-list"><!-- wp:list-item -->
<li><strong>決意</strong>：行きたい気持ちが最高潮に達し、お小遣いが貯まったタイミングで、ついに来店を決意します。</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>行動</strong>：登録していたLINEを開き、予約を入れます。</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

