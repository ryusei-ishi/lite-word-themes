<!-- wp:heading {"level":3,"className":"is-style-h_03"} -->
<h3 class="wp-block-heading is-style-h_03" id="451f68dd-70ce-4f31-980e-5aaf80189495">簡易的ターゲット（ペルソナ）設定</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p id="b7daaeb1-e9cc-4d29-ac2c-ac27ce040d59" class="">LiteWordを本当に必要としているのは誰なのか、改めて考える必要がありました。 そして、結論としてターゲット（ペルソナ）を「WordPress初心者」に設定しました。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="cc18f023-b0bb-48e6-8619-dedeb8b1a539" class="">なぜ、そのように考えたのか。 実は、私は8年前からWordPressの講師をしており、多くの生徒さんがいます。その生徒さんたちに「LiteWordを作ったので、ぜひ無料で使ってみてください」と伝えたのですが、誰一人として使ってはくれませんでした。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="f607d62c-2970-4e81-99f7-b1f3939dc97a" class="">理由はとてもシンプルで、「今は必要ないから」です。 彼らはすでに他のWordPressテーマでご自身のホームページを完成させています。わざわざ時間をかけて、完成したホームページをLiteWordで作り直す必要はありません。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="d649426d-60c4-4222-9410-89675f823fa1" class="">この経験から、LiteWordを使ってくださるのは、すでにホームページを持っている方ではなく、「まさに今、テーマを探している方」だと気づきました。 そして、テーマ探しで困っている方の多くは、これからWordPressを始めたいと考えている初心者の方々なのです。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3,"className":"is-style-h_03"} -->
<h3 class="wp-block-heading is-style-h_03" id="2439b913-b61d-4fa1-b0d5-29a529433e94">なぜプロモーションにYouTubeを選んだのか？</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p id="dbf9cd12-6765-4005-bfee-832e0c2d2c28" class="">数あるSNSの中で、なぜ私がYouTubeを選んだのでしょうか。 結論から言うと、<strong>ターゲット（ペルソナ）がWordPressを操作する環境が「パソコン」だから</strong>です。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="694c77b7-a083-4f5e-8b53-6cc413195763" class="">LiteWordテーマをご利用いただくには、サイトからファイルをダウンロードする必要があります。そのため、お客様がパソコンを操作しているタイミングでアプローチすることが不可欠です。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="faffb6dc-adc6-47a6-b5e6-171fb60a8d84" class="">仮にスマートフォンやタブレットでLiteWordを知っていただいたとしても、「後でパソコンを開いてサイトにアクセスし、ダウンロードする」という手間が発生します。残念ながら、その手間をかけてまで行動してくださる方は、そう多くはありません。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="5af0a627-4701-4d51-b6e8-ac02409938f3" class="">では、「WordPressの作業中（＝パソコンの利用中）に、ユーザーが最も参考にするメディアは何か？」と考えたとき、それはYouTubeでした。 WordPressで分からないことがあれば、多くの方がYouTubeで解説動画を検索します。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="6d3a56e0-522b-4d14-a7b4-35fb2a96731d" class="">そこで私たちは、<strong>「初心者のためのWordPressレッスン」のような動画を視聴しながら、その流れで自然にLiteWordをダウンロードしてもらう</strong>、という戦略を立てました。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="16dbf795-eb21-418b-b91b-8fd1f5bc055f" class="">この狙いは見事に的中しました。 実際に、他のSNS媒体と同じ広告費をかけて効果を比較検証しましたが、十分な反応を得られたのはYouTubeだけでした。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3,"className":"is-style-h_03"} -->
<h3 class="wp-block-heading is-style-h_03" id="92fe7728-ad3f-4e41-8455-c40bc55442a6">リード獲得っぽさを無くす自然な流れに拘る</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p id="f759f837-f198-47f1-a406-dc20db8c09c5" class="">次にこだわったのは、「リード獲得が目的だ」と視聴者に感じさせない、自然な流れを構築することでした。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="795ed78d-a0bf-49b5-b3b6-17ed0688269d" class="">これは、YouTubeで公開している「WordPress初心者向けレッスン」動画の中で実現しています。動画の制作プロセスに、LiteWordテーマのダウンロードを自然な形で組み込んだのです。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="ebac4996-2593-46b2-9336-f4fd32bd741a" class="">まず、この動画の趣旨は、あくまで「WordPressの使い方を覚えてもらうこと」です。インストールから美しいページを作成するまでの一連の流れを解説することが目的であり、LiteWordをダウンロードさせること自体がゴールではありません。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="24b5ec9b-21bc-4938-a735-7bf6d52e6ea7" class="">レッスンを進めていくと、サイトデザインを整える段階で、どうしてもWordPressテーマが必要になります。そこで、「この先の作業に進むために、こちらからテーマをダウンロードしましょう」というように、ごく自然な流れで案内するのです。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="a38581a7-1f67-4014-b3c2-c9d2f832b33b" class="">具体的な手順も、「ここにメールアドレスを登録してボタンを押すと、テーマがメールで届きます。そのZIPデータをWordPressにインストールしてください」と、動画内で操作を見せながら解説します。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="080ea0ca-3c2f-447f-b0fc-482b1df00d5f" class="">この方法により、視聴者はいやらしさを感じることなく、レッスンの一環として自然にダウンロード（メールアドレスの登録）を行ってくれます。結果として、売り込み感を一切出さずにリードを獲得することができました。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3,"className":"is-style-h_03"} -->
<h3 class="wp-block-heading is-style-h_03" id="94bb22a5-d079-498a-b30a-94bbb614f55a">信頼の担保はまずはYouTubeでできる</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p id="5b3c2ccd-a485-416a-9639-3a75c387a96a" class="">次に、私が「YouTubeは本当にすごい」と感じた点についてお話しします。 それは、<strong>「信頼性」を自然な形で獲得しやすい</strong>という点です。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="cea1a788-b39c-469f-845b-f0eb14695d0b" class="">私は動画に顔出しはしていませんが、自身の声で「ここをクリックしてください」「次はここを操作します」と丁寧に解説しています。すると、視聴者は動画を見ているうちに、話し手である私に対して親近感を抱き、信頼を寄せてくれるようになります。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="f2931d55-9357-40dc-89bb-9a79919adac8" class="">つまり、<strong>動画を視聴している段階で、すでにある程度の信頼関係が構築されている</strong>のです。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="71553513-373a-40ab-b778-db6be714d993" class="">そのため、動画から私たちのサイトを訪れた方は、「このサイトは本当に安全だろうか？」といった不安を感じにくくなります。事前に築いた信頼があるからこそ、安心してテーマをダウンロードしていただける。この状態を作り出せるのが、YouTubeの素晴らしい点だと感じています。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="e07d0963-36c6-46a9-bf61-06a0e960116b" class="">例えばブログの場合、ユーザーはテキスト情報を読むだけなので、書き手への親近感は湧きにくい傾向があります。「欲しい情報がここにあった」という事実の確認で終わってしまうことが多いでしょう。 もちろん、文章だけで人間性を伝えられる素晴らしいブログも存在しますが、声に比べるとテキストは温かみやニュアンスが伝わりにくい側面もあります。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="b071fb89-a788-4106-861c-239707c3fa11" class="">その点、YouTubeは声のトーンや話し方を通じて、テキストでは伝えきれない「人柄」までも伝えることができます。この「信頼を醸成する力」こそが、YouTubeの最大の強みだと確信しています。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3,"className":"is-style-h_03"} -->
<h3 class="wp-block-heading is-style-h_03" id="c3856819-db69-4c6b-b63e-5e81ccfb44f7">広告の発行が簡単過ぎるYouTubeプロモーション広告</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p id="35368710-c18d-4836-9487-0f8fe5f15db8" class="">YouTubeが優れていると感じる点が、もう一つあります。 それは、<strong>「YouTubeプロモーション」という広告機能が、非常に簡単に使える</strong>ことです。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="b3bec5ab-6fb4-42ec-8d1e-b59f1283c67f" class="">何が簡単かと言うと、広告の配信設定を、ほぼ全てAIが自動で行ってくれるからです。 私たちがやるべきことは、基本的に「広告に出したい動画を選ぶ」ことと、「予算（1,000円や2,000円程度から可能）を設定する」ことだけ。複雑な設定は一切不要です。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="fdd12f71-c3c2-4fa4-9753-357964b78d23" class="">例えば、「WordPress初心者向けレッスン」というタイトルの動画を広告に設定したとします。するとAIが、そのテーマに興味を持ちそうなユーザーを自動で探し出し、的確に広告を届けてくれます。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="ec08d088-0210-437f-b3b3-4a5705536053" class="">その結果、広告を見たユーザーはスムーズにチャンネル登録や動画視聴をしてくださり、そこから自然な流れでテーマのダウンロードへと繋がっていきます。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="0e417824-6d00-4a36-a50d-57aa5f5b184c" class="">ここで重要なのは、これはGoogle管理画面から出稿する複雑な「Google広告」ではなく、YouTubeの操作画面内にある「プロモーション」という機能だということです。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="ca345a7a-4a32-4b24-b06a-c4ce408a315c" class="">私自身、広告の運用は得意ではなく、面倒だと感じてしまうタイプなのですが、この機能は驚くほど簡単でした。だからこそ、ビジネスを始めたばかりの方には、まずこの「YouTubeプロモーション広告」から試してみることを強くお勧めします。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="c1b6332e-5d1f-46a3-a194-b646635ee2bb" class="">弱点は、細かい設定ができません。<br>現段階では年代のみ設定できます。<br>それ以外の地域や趣味などの細かな条件を設定していきたい場合は、Google広告から行う必要があります。そして、少し広告の値段が高くなってしまいます。</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3,"className":"is-style-h_03"} -->
<h3 class="wp-block-heading is-style-h_03" id="61d1bba5-bdbd-4844-bbc1-5a6fbef90567">サイトは分かりやすさを重視</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p id="68a3fd4b-776b-47d0-b832-7437518697ae" class="">次に、LiteWordのダウンロードページについてご説明します。 このページで最も重要視したのは、徹底した「分かりやすさ」です。初めは非常にシンプルな構成でしたが、データを見ながら段階的に改善を加えていきました。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="f5b2a281-388c-428d-981c-51aeab7910db" class="">ランディングページの目的はサービスによって異なりますが、LiteWordの場合は「テーマをダウンロードしてもらうこと」がゴールです。 そのためには、<strong>ダウンロードに必要な情報</strong>と、迷わず操作できる「ダウンロードのしやすさ」が不可欠でした。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="d0e2fe68-3740-4f86-9a6a-7efc41538bf2" class="">具体的には、ページを開いた瞬間に「LiteWordのサイトである」ことが明確に分かり、そして「ダウンロードはこちら」のボタンが必ず画面内に表示されるように設計しました。これだけでも、ダウンロードされる確率は大きく変わります。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="5582cfc6-009c-4c5d-aea7-09901eac6f35" class="">また、「LiteWordとは何か？」が端的に分かる説明も加えました。YouTubeである程度は解説していますが、動画を見ていない方が直接サイトに来ることも想定し、ごく短い文章で要点をまとめています。 さらに、システムの内容を文章だけで伝えるのは難しいため、後から<strong>自動再生されるデモ動画などを埋め込み</strong>、視覚的な分かりやすさを補強していきました。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="ef21ab51-3512-4ecf-921f-35675fbc946e" class="">こうした改善を重ねるにつれてダウンロード率は着実に向上しましたが、YouTube動画内でダウンロードを促す強力な導線があったため、サイト公開後わずか数日で最初のダウンロードは発生していました。</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p id="c8f34233-77e1-44fe-891f-80513d407d0a" class="">全体の流れとしては、まず「サイトへのアクセスとダウンロード」という基本的な仕組みを作り、その後にページの改善を重ねて成約率を高めていく、という段階的なアプローチで進めました。</p>
