<p id="3df051b6-7a3a-4318-a3a0-9dc3acd0f88c" class="">試行錯誤の末に、<strong>私の「LiteWord」というサービスにとっての最適解</strong>、それが<strong>YouTube</strong>でした。<br>それから2週間、私はYouTubeからの集客導線づくりに全力を注ぎました。「LiteWord」の魅力を動画で伝え、それを見た人がサイトを訪れ、ダウンロードしてくれる。この流れを徹底的に作ったのです。</p>
